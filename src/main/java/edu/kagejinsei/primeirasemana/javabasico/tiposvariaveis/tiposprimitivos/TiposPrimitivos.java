package edu.kagejinsei.primeirasemana.javabasico.tiposvariaveis.tiposprimitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        int idade = 20; // Armazena números inteiros
        float valor = 45.45F; // Armazena números com ponto flutuante
        double salario = 2550.55; // Armazena números com ponto flutuante de maior capacidade
        char sexo = 'M'; // Armazena um caractere entre aspas simples
        boolean verdadeiro = true; // Armazena valoes booleanos (verdadeiro ou falso)

        System.out.println("Valor: " + idade);
        System.out.println("Valor: " + valor);
        System.out.println("Valor: " + salario);
        System.out.println("Valor: " + sexo);
        System.out.println("Valor: " + verdadeiro);
    }
}
